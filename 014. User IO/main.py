# 출력 print

# 큰따옴표로 둘러싸인 문자열은 + 연산과 같다.
print("life" "is" "too short")
print("life"+"is"+"too short")

# 문자열 띄어쓰기는 콤마로 한다.
print("life", "is", "too short")

# 한 줄에 결과값 출력하기 end
# end로 끝 문자를 지정할 수 있다.
for i in range(10):
    print(i, end=' ')  # end로 끝문자를 ' ' 으로 만들었다.
